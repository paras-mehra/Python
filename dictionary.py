# <================ Python Dictionary & Sets ===============>

# Python Dictionary is an unordered collection of items. While other compound data types have only value as an element, a dictionary has a key:Value pair.
# ===================================================================================

# ------------
# Python Sets
# ------------

a = {1,2,3,4,5,6,12,2,31,1}
print(a)
print(type(a))
print(a[0]) # Indexing is not Supported in Sets 

# ==================================================================================

# -----------------
# Python Dictionary
# -----------------

marks = {"Bharat":86,"Paras":85,"Karan":96, 25:92, 30:85}
print(marks)
print(marks["Karan"])

marks["Raman"] = 98
marks["Bharat"] = 80
print(marks)

